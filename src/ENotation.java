public class ENotation {
    public static void main(String [] args){
        double statement1 = 3.287E6;
        double statement2 = -9.7865E12; // Returns the same value
        double statement3 = 7.65491E-3;
        double statement4 = -153.5432E-5;
        System.out.println("In java, 3.287E6 is equal to "+ statement1);
        System.out.println("In java, -9.7865E12 is equal to "+ statement2);
        System.out.println("In java, 7.65491E-3 is equal to "+ statement3);
        System.out.println("In java, -153.5432E-5 is equal to "+ statement4);
    }
}
